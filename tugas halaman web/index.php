    <p>
    <b>Kucing</b> disebut juga <b>kucing domestik</b> 
    <sup><a href="">[4][5]</a></sup>
    atau <b>kucing rumah</b>  
    <a href="">(nama ilmiah:</a> 
    <i><b>Felis silvestris catus atau Felis catus</b></i>) adalah sejenis <a href="">mamalia karnivora</a> dari <a href="">keluarga Felidae.</a> Kata "kucing" biasanya merujuk kepada "kucing" yang telah dijinakkan,<sup><a href="">[6]</a></sup> tetapi bisa juga bisa merujuk kepada 
    <a href="">"kucing besar"</a> seperti <a href="">singa</a> dan <a href="">harimau</a> yang juga termasuk jenis kucing.
    </p>

    <p>
    Kucing telah berbaur dengan kehidupan 
    <a href="">manusia</a> paling tidak sejak 5.000 tahun SM, dengan ditemukannya <a href="">kerangka</a> kucing di <a href="">Pulau Siprus.</a>
    <sup><a href="">[7]</a></sup>
    Sejak zaman 3.500 SM, orang <a href="">Mesir kuno</a> telah menggunakan kucing untuk menjauhkan <a href="">tikus</a> atau <a href="">hewan pengerat</a> lain dari <a href="">lumbung</a> yang menyimpan hasil panen. <sup><a href="">[8]</a></sup>
    </p>

    <p>
    Saat ini, kucing adalah salah satu <a href="">hewan peliharaan</a> terpopuler di dunia.
    <sup><a href="">[9]</a></sup>
    Kucing yang garis keturunannya tercatat secara resmi sebagai kucing <a href="">trah</a>atau <a href="">galur murni</a> <b><i>(pure breed)</i></b>, seperti <a href="">persia, siam, manx, dan sphinx.</a> Kucing seperti ini biasanya dibiakkan di tempat pemeliharaan hewan resmi. Jumlah kucing ras hanyalah 1% dari seluruh kucing di dunia, sisanya adalah kucing dengan keturunan campuran, seperti <a href="">kucing liar</a> atau kucing kampung. Kucing juga merupakan jenis hewan peliharaan yang banyak diminati karena tingkah lakunya yang lucu.
    </p>

   
